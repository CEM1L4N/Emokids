import Foundation
import SwiftUI

extension View {
    func whitetext15() -> some View{
        self.font(.system(size: 15)).foregroundColor(.white)
    }
    func whitetext24() -> some View{
        self.font(.system(size: 24)).foregroundColor(.white)
    }
    
     func whitetext28() -> some View{
        self.font(.system(size: 28)).foregroundColor(.white)
    }
    
    func whitetext36() -> some View{
        self.font(.system(size: 36)).foregroundColor(.white)
    }
    
}
