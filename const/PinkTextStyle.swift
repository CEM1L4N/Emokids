import Foundation
import SwiftUI

extension View {
    
    func pinktext24() -> some View{
        self.font(.system(size: 24)).foregroundColor(.pinkClr)
    }
    func pinktext41() -> some View{
        self.font(.system(size: 41)).foregroundColor(.pinkClr)
    }
}
